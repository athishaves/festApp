package com.athish_works.festdynamicapp;

public class EventsClass {
    private String cont3Name;
    private String fee;
    private String schDate;
    private String cont3No;
    private String cont2Name;
    private String teamSize;
    private String cont1No;
    private String cont2No;
    private String imageURL;
    private String name;
    private String schTime;
    private String cont1Name;
    private String desc;

    public EventsClass() {
    }

    public EventsClass(String cont3Name, String fee, String schDate, String cont3No, String cont2Name, String teamSize, String cont1No, String cont2No, String imageURL, String name, String schTime, String cont1Name, String desc) {
        this.cont3Name = cont3Name;
        this.fee = fee;
        this.schDate = schDate;
        this.cont3No = cont3No;
        this.cont2Name = cont2Name;
        this.teamSize = teamSize;
        this.cont1No = cont1No;
        this.cont2No = cont2No;
        this.imageURL = imageURL;
        this.name = name;
        this.schTime = schTime;
        this.cont1Name = cont1Name;
        this.desc = desc;
    }

    public String getCont3Name() {
        return cont3Name;
    }

    public void setCont3Name(String cont3Name) {
        this.cont3Name = cont3Name;
    }

    public String getFee() {
        return fee;
    }

    public void setFee(String fee) {
        this.fee = fee;
    }

    public String getSchDate() {
        return schDate;
    }

    public void setSchDate(String schDate) {
        this.schDate = schDate;
    }

    public String getCont3No() {
        return cont3No;
    }

    public void setCont3No(String cont3No) {
        this.cont3No = cont3No;
    }

    public String getCont2Name() {
        return cont2Name;
    }

    public void setCont2Name(String cont2Name) {
        this.cont2Name = cont2Name;
    }

    public String getTeamSize() {
        return teamSize;
    }

    public void setTeamSize(String teamSize) {
        this.teamSize = teamSize;
    }

    public String getCont1No() {
        return cont1No;
    }

    public void setCont1No(String cont1No) {
        this.cont1No = cont1No;
    }

    public String getCont2No() {
        return cont2No;
    }

    public void setCont2No(String cont2No) {
        this.cont2No = cont2No;
    }

    public String getImageURL() {
        return imageURL;
    }

    public void setImageURL(String imageURL) {
        this.imageURL = imageURL;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSchTime() {
        return schTime;
    }

    public void setSchTime(String schTime) {
        this.schTime = schTime;
    }

    public String getCont1Name() {
        return cont1Name;
    }

    public void setCont1Name(String cont1Name) {
        this.cont1Name = cont1Name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
